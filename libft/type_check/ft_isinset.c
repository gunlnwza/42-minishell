// same as (c == set[0] || c == set[1] || ... || c == set[n - 1])
int	ft_isinset(int c, char *set, int n)
{
	int	i;

	i = 0;
	while (i < n)
	{
		if (c == set[i])
			return (1);
		i++;
	}
	return (0);
}
