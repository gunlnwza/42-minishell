#include <unistd.h>
#include "t_list.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *));

// if del function is NULL, do nothing with the lst->content
void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*next_node;

	while (*lst)
	{
		next_node = (*lst)->next;
		ft_lstdelone(*lst, del);
		*lst = next_node;
	}
}
